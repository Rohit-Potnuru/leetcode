# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
class BSTIterator:

    def __init__(self, root: Optional[TreeNode]):
        self.nextNode = self.getNext(root)

    def getNext(self, curr: Optional[TreeNode]) -> Optional[TreeNode]:
        while curr:
            if curr.left:
                predecessor = self.getPredecessor(curr)
                if predecessor.right:
                    predecessor.right = None
                    return curr
                else:
                    predecessor.right = curr
                    curr = curr.left
            else:
                return curr
        return curr

    def getPredecessor(self, node: Optional[TreeNode]) -> Optional[TreeNode]:
        curr = node.left
        while curr.right and curr.right != node:
            curr = curr.right
        return curr

    def next(self) -> int:
        curr = self.nextNode
        self.nextNode = self.getNext(curr.right)
        return curr.val

    def hasNext(self) -> bool:
        return self.nextNode != None


